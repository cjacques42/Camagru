function login()
{
	var div = document.getElementById('user_space');
	if (div.style.display == "block")
		div.style.display = "none";
	else
		div.style.display = "block";
}
function signup()
{
	var div = document.getElementById('user_space');
	if (div.style.display == "block")
		div.style.display = "none";
	else
		div.style.display = "block";
}